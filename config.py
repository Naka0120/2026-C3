# config.py
import numpy as np

# --- グリッドと計算設定 ---
NX = 400          # x方向のグリッド数 (流路の長さ)
NY = 100          # y方向のグリッド数 (流路の幅)
MAX_ITERS = 4000  # シミュレーションの最大ステップ数
PLOT_FREQ = 200   # 画像として出力する頻度

# --- 物理パラメータ (格子単位系: Lattice Units) ---
# U_IN: 格子単位系における流入速度。
# LBM（格子ボルツマン法）では、音速が 1/sqrt(3) (約0.577) となります。
# 圧縮性の影響を無視して安定した計算を行うため、マッハ数 (U_IN / 音速) が
# 0.1 以下（すなわち U_IN < 0.05 程度）になるように設定する必要があります。
U_IN = 0.05       

# RE: レイノルズ数 (Reynolds Number)。流体の慣性力と粘性力の比を表します。
# 障害物後方にカルマン渦（周期的な渦）を綺麗に発生させるため、100〜200付近に設定します。
# この渦の周期的な放出と、パイプ内の気柱の共鳴が連成することでパイプオルガンの音が鳴ります。
RE = 150.0        

# --- パイプオルガン用の障害物設定（複数パイプ・和音対応） ---
PIPE_Y_TOP = 40      # パイプの開口部のY座標（全パイプ共通、上向き）
THICKNESS = 5        # パイプの壁の厚さ

# 障害物の各種レンジ設定（UIのスライダー制限などに適用）
# (最小値, 最大値) の形式で指定します
PIPE_WIDTH_RANGE = (5, 50)   # パイプの内径（幅）の可変レンジ
PIPE_DEPTH_RANGE = (10, 80)  # パイプの深さ（長さ）の可変レンジ
WIND_MS_RANGE = (0, 30)      # 風速(m/s)の可変レンジ

# 和音の構成比率（主音に対する深さ・太さの比率）
# 主音(ド)=1.0, 第3音(ミ)≒0.8, 第5音(ソ)≒0.66
CHORD_RATIOS = [1.0, 0.8, 0.66]
PIPE_SPACING = 30    # パイプ間の隙間（グリッド数）

# UIで操作する「主音」の初期値
BASE_PIPE_X = 140    # 左端の開始X座標
BASE_PIPE_WIDTH = 10
BASE_PIPE_DEPTH = 30

# 和音モードフラグ
CHORD_MODE = False

# 現在有効なパイプのリスト（シミュレーション・描画で参照）
# 要素の形式: {'x': X座標, 'width': 幅, 'depth': 深さ}
PIPES = [{'x': BASE_PIPE_X, 'width': BASE_PIPE_WIDTH, 'depth': BASE_PIPE_DEPTH}]

# --- 物理スケール変換 ---
# DX_REAL: 1グリッドが現実世界の何メートル (m) に相当するか。
# この値を小さくすると、パイプの物理的なサイズが小さくなり、高い音（高い周波数）が出ます。
DX_REAL = 0.01          

# LBM_TO_MS: 格子単位の速度(LBM単位)を現実の速度(m/s)に変換する係数。
# 現実の音速 (約340 m/s) とLBMでの音速 (1/sqrt(3)) の比率から算出されます。
LBM_TO_MS = 340.0 * np.sqrt(3)  

# --- LBM用 導出パラメータ ---
# NU: 格子単位系における動粘性係数 (ν = U * L / Re)。
# 特性長 L は主音パイプ幅として計算し、レイノルズ数 RE を満たすように粘性を決定します。
NU = U_IN * BASE_PIPE_WIDTH / RE

# TAU: 緩和時間 (τ)。衝突過程で分布関数が平衡分布に近づく速さを決定します。
# 数値安定性のため、TAU > 0.5 である必要があります。
TAU = 3.0 * NU + 0.5