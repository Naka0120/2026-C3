# config.py

# --- グリッドと計算設定 ---
NX = 400          # x方向のグリッド数 (流路の長さ)
NY = 100          # y方向のグリッド数 (流路の幅)
MAX_ITERS = 4000  # シミュレーションの最大ステップ数
PLOT_FREQ = 200   # 画像として出力する頻度

# --- 物理パラメータ (格子単位系: Lattice Units) ---
U_IN = 0.05       # 流入速度 (マッハ数0.1以下に抑えるため小さく設定)
RE = 150.0        # レイノルズ数 (カルマン渦が綺麗に発生する100〜200付近に設定)

# --- 障害物 (円柱) の設定 ---
CYLINDER_R = 10   # 円柱の半径
CYLINDER_X = 80   # 円柱の中心 x座標
CYLINDER_Y = 50   # 円柱の中心 y座標

# --- LBM用 導出パラメータ ---
# 動粘性係数 ν = U * L / Re (Lは特性長 = 円柱の直径)
NU = U_IN * (CYLINDER_R * 2.0) / RE
# 緩和時間 τ = 3 * ν + 0.5
TAU = 3.0 * NU + 0.5